const assert = require('assert');
const mod = require('..');

function assertGenerateString(expected, time, ...bytes) {
  assert.equal(mod.generateString(time, ...bytes), expected);
}

assertGenerateString('00000000000000000000000000', 0, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00);
assertGenerateString('00000000000000000000000001', 0, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x01);
assertGenerateString('0000000000000000000000000Z', 0, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x1f);
assertGenerateString('0000000000000000000000007Z', 0, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0xff);
assertGenerateString('7ZZZZZZZZZ0000000000000000', mod.MAX_TIME, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00);
assertGenerateString('0000000000ZZZZZZZZZZZZZZZZ', mod.MIN_TIME, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff);
assertGenerateString('7ZZZZZZZZZZZZZZZZZZZZZZZZZ', mod.MAX_TIME, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff);
assertGenerateString('', -1, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00);
assertGenerateString('', 0, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00);

console.log("ok");
